#include "philo.h"

void	ft_putendl_fd(char *s, int fd)
{
	while (*s)
	{
		write(fd, s, 1);
		s++;
	}
	write(fd, "\n", 1);
}

void	ft_error(char *msg)
{
	if (msg)
		ft_putendl_fd(msg, 2);
	exit(EXIT_FAILURE);
}
